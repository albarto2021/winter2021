package day03;

public class C6_IncerementDecrement {

	public static void main(String[] args) {
		
		int number=10;
		// bir variable'in degerini toplayarak artirmak için 3 yontem kullanabilirim.
		
		// 1. yontem (acemi yontem)
		
		number = number + 5;
		
		// 2. yontem (usta isi)
		
		number += 8;
		
		// 3. yontem sadece 1 (bir) artirmak icin
		
		number++;
		
		// bu yontemler sadece toplama islemine has degildir tum operatorler kullanilabilir.
		// 3. yontem sadece toplama cikarma icin gecerlidir.
		
		// number'i 6 eksiltin
			number -= 6;
			
		// number'i ikiye bolun			
			number /= 2;
			
		// number'i ucle carpýn			
			number *= 3;
			
		// number'i 1 eksiltin
			number--;	
		

	}

}
