package day01_variable;

public class C5_Veriables4 {

	public static void main(String[] args) {
		int sayi1=10;
		int sayi2=4;
		double sayi3=4;
		
		System.out.println(sayi1/sayi2); //10/4=2  tam sayı tam sayıya bölersek tam sayı çıkar. yani 2 çıkar
		
		System.out.println(sayi1/sayi3); //10/4=2.5 sayi3 double olduğu için sonucu virgüllü olarak verir.

	}

}
